                <h1>What is Mystice?</h1>
                
                <p>Mystice is an online database where anyone can add a superhero or a team.</p>
                <p>The main purpose of this website is to order and store information on each superhero and each team.
                This is to insure that whenever a charecter or storyline is being developed you can store all the confirmed canon information here to avoid confusion on what is canon and what is non-canon.</p>
                
                <h1>Adding a superhero or a team</h1>
                
                <p>Go to the "New Character" page or "New Team" page to add a new record.</p>
                <p>A character can only be a member of one team, if it is not a member of any team then pick the default "No Team" from the Team dropdown menu.
                If you want your data edited or deleted then contact me by clicking the link in the footer.</p>
                
                <h1>Searching and filtering</h1>
                
                <p>Go to the "Search" page and enter your criteria to filter the results.</p>
                <p>Our search function allows a wide selection of filters and search criteria,
                By default it will show all characters.</p>
